/**
 * Card primitive — vendored shadcn/ui pattern, adapted to our project.
 *
 * Premium light theme: white surface, slate-200 border, subtle
 * shadow-sm. Consumers can override with the `className` prop but the
 * defaults already match every hand-rolled node card in the graph.
 */
import * as React from "react";
import { cn } from "@/lib/utils";

export const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-xl border border-slate-200 bg-white text-slate-900 shadow-sm",
      className,
    )}
    {...props}
  />
));
Card.displayName = "Card";

